def map_to_abbr(team_name: str) -> str | None:
    """Map a free-form team name (city, full name, etc.) to its 3-letter abbreviation."""
    if not team_name:
        return None
    lower = team_name.lower()
    for full, abbr in TEAMS.items():
        if lower in full.lower() or full.lower() in lower:
            return abbr
    return None
